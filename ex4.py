sla = input ('Digite algo :')
print('O tipo primitivo desse valor é: {}'.format(sla), type(sla))
print('Possui so espaços {} ?' , sla.isspace())
print('É um numero ?', sla.isnumeric())
print('É alfabético ?', sla.isalpha())
print('É alfanumérico :', sla.isalnum())
print('É maiúsculas ?', sla.isupper())
print('É minúsculas ?', sla.islower())
print('É capitalizado', sla.istitle())



